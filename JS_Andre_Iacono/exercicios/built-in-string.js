let msg = 'Hey, my name is Tiago'

console.log(msg.startsWith('Hey')) // Confere se o que vc digitou bate com o conteúdo inicial da variável em questão.

console.log(msg.length) // Diz a quantidade de caracteres existentes na variável em questão... (espaços em branco, também são contabilizados)

console.log(msg.includes('meu')) // Verifica se o valor que vc digitou existe dentro da variável em questão ou não...

console.log(msg.endsWith('go')) // Confere se o que vc digitou bate com o conteúdo final da variável em questão.